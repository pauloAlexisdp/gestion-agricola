    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       //pueden acceder desde supervisor o dueño
       //volver al dueño
       if(this.panel_anterior.equals("busquedadueño")){
           this.controladorVista.SeleccionarPanel("dueño");
       }else if(this.panel_anterior.equals("busquedasupervisor")){//volver al supervisor
            this.controladorVista.SeleccionarPanel("supervisor");
       }
       
       

    }//GEN-LAST:event_jButton4ActionPerformed

    private void realizarBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_realizarBusquedaActionPerformed
        
        
        if(this.RespuestaRutificador.getText() != null){
            String[][] busqueda = ControladorAsistencia.realizarBusqueda(this.RespuestaRutificador.getText());
            if(busqueda != null){
                controladorVista.getAsistencia().reiniciarTabla();
                controladorVista.getAsistencia().setDatos_tabla(busqueda);
                controladorVista.getAsistencia().cargarDatosTabla();
                controladorVista.SeleccionarPanel("asistencia");
            }
            else{
                JOptionPane.showMessageDialog(null, " el rut ingresado no fue encontrado", "Búsqueda", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Faltan llenar casillas.", "Búsqueda", JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_realizarBusquedaActionPerformed


